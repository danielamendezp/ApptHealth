    /**
    * Carga las categorías de servicio médico en un JComboBox.
    */
   public void cargarServicioCategoria() {
       // Crear una instancia del BLL para manejar las categorías de servicio médico
       CategoriaServicioMedicoBLL categoriaServicioMedicoBLL = new CategoriaServicioMedicoBLL();

       // Obtener la lista de categorías de servicio médico
       List<CategoriaServicioMedico> listaCategoria = categoriaServicioMedicoBLL.getListaCategoriaServicioMedico();

       // Agregar cada categoría de servicio médico al JComboBox
       for (CategoriaServicioMedico categoria : listaCategoria) {
           jCboCategoriaServiciosMedicos.addItem(categoria);
       }
   }


