    private void jBotConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotConsultarActionPerformed
        CategoriaServicioMedicoBLL categoria= new CategoriaServicioMedicoBLL();
        ServiciosMedicos mServiciosMedicos=accionServicio.buscarServiciosMedicosPorNombre(jTxtDescripcion.getText());
        
        if(mServiciosMedicos!=null){
            jCboCategoriaServiciosMedicos.setSelectedItem(categoria.buscarCategoriaServicioMedico(mServiciosMedicos.getCategoriaServiciosMedicos()));
            jTxtFactorCosto.setText(String.valueOf(mServiciosMedicos.getFactorCosto()));
        }else{
            JOptionPane.showMessageDialog(null, "Faltan Datos");
        }
            
    }//GEN-LAST:event_jBotConsultarActionPerformed

    private void jBotEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotEliminarActionPerformed
        int option = JOptionPane.showConfirmDialog(null, "¿Seguro que desea eliminar?", "Confirmación", JOptionPane.YES_NO_OPTION);
        if(option>1){
            ServiciosMedicos mServiciosMedicos=accionServicio.buscarServiciosMedicosPorNombre(jTxtDescripcion.getText());
             if(mServiciosMedicos!=null){
                 accionServicio.eliminarServiciosMedicos(String.valueOf(mServiciosMedicos.getIdServicio()));
             }
        }
    }//GEN-LAST:event_jBotEliminarActionPerformed

    private void jBotModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotModificarActionPerformed
        ServiciosMedicos mServiciosMedicos=accionServicio.buscarServiciosMedicosPorNombre(jTxtDescripcion.getText());
        if(ValidadorFormatos.validarDouble(jTxtFactorCosto.getText())){
            if(mServiciosMedicos!=null){
                ServiciosMedicos serviciosMedicos=new ServiciosMedicos(mServiciosMedicos.getIdServicio(), jTxtDescripcion.getText(), 
                                               Double.parseDouble(jTxtFactorCosto.getText()), ((CategoriaServicioMedico)jCboCategoriaServiciosMedicos.getSelectedItem()).getIdCategoria());
                accionServicio.modificarServiciosMedicos(serviciosMedicos);
           }else{
               JOptionPane.showMessageDialog(null, "Faltan Datos");
           }
        }else{
            JOptionPane.showMessageDialog(null, "Factor Costo mal escrito");
        }
    }//GEN-LAST:event_jBotModificarActionPerformed

    private void jBotAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotAgregarActionPerformed

        if(ValidadorFormatos.validarDouble(jTxtFactorCosto.getText())){
            int consecutivo=accionServicio.consecutivo();
            String nombre=jTxtDescripcion.getText();
            double factor= Double.parseDouble(jTxtFactorCosto.getText());
            int cat= ((CategoriaServicioMedico)jCboCategoriaServiciosMedicos.getSelectedItem()).getIdCategoria();
            if(!nombre.isEmpty()&&factor>-1&&cat>-1&&consecutivo>0){
            ServiciosMedicos serviciosMedicos=new ServiciosMedicos(consecutivo, nombre, 
                                              factor , cat);
            accionServicio.agregarServiciosMedicos(serviciosMedicos);
            }else{
                JOptionPane.showMessageDialog(null, "Faltan Datos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Factor Costo mal escrito");
        }
    }//GEN-LAST:event_jBotAgregarActionPerformed

