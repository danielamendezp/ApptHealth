    private void inicializarJTable(){
        //Cuando se marca cualquier celda 
        //hace que se marque la fila completa
        jTable.setRowSelectionAllowed(true);
        
        //Aplicar el DefaultTableModel a la Tabla de Datos        
        String[] arregloTitulosTabla = {"Padecimiento", "Tratamiento Fármaceutico", "Servicio Médico", "Médico Tratante"};
        oModeloTablaDatos = new DefaultTableModel(arregloTitulosTabla, 0);
        jTable.setModel(oModeloTablaDatos);
    }
/**
 * Carga los datos en la tabla de registros de padecimientos para un paciente.
 */
    private void cargarJTable() throws ParseException {        
        //Definir objeto Vector para cargar datos
        String[] vDatosTabla = new String[4];
        
        //Limpiar los rows
        this.oModeloTablaDatos.setRowCount(0);
        Paciente paciente=accionPaciente.buscarPaciente(this.jTxtCedula.getText()); 
        //Obtener la lista de RegistroPadecimientos del BLL
        List<RegistroPadecimiento> listaRegistroPadecimientos = registro.getListaRegistroPadecimientos(String.valueOf(expediente.buscarExpedienteMedicoPorCedula(paciente.getCedula()).getNumExpediente()));
       
            for (RegistroPadecimiento oRegistroPadecimiento : listaRegistroPadecimientos) {
                String producto="";
                String servicio="";
                String enfermedad="";
                if(oRegistroPadecimiento.getProducto()!=null){
                    producto=oRegistroPadecimiento.getProducto().toString();
                }
                if(oRegistroPadecimiento.getServiciosMedicos()!=null){
                    servicio=oRegistroPadecimiento.getServiciosMedicos().toString();
                }
                if(oRegistroPadecimiento.getEnfermedad()!=null){
                    enfermedad=oRegistroPadecimiento.getEnfermedad().toString();
                }
                //Leer los datos del objeto que se esta iterando
                vDatosTabla[0] = enfermedad;
                vDatosTabla[1] = producto;
                vDatosTabla[2] = servicio;
                vDatosTabla[3] = oRegistroPadecimiento.getDoctor().toString();
                
                //Agregar la instancia al objeto modelo
                oModeloTablaDatos.addRow(vDatosTabla);
            }
            jTable.setModel(oModeloTablaDatos);     
    }
