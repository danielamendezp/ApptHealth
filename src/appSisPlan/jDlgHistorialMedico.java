    private void jTxtCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTxtCedulaActionPerformed
        jTxtNoExpediente.setText("");
        if(jTxtCedula.getText().length()==9){
            if(accionPaciente.buscarPaciente(jTxtCedula.getText())!=null){

                Paciente paciente=accionPaciente.buscarPaciente(this.jTxtCedula.getText());
                jTxtNombrePaciente.setText(paciente.getNombreApellidos());
                jTxtEdad.setText(String.valueOf(paciente.edad()));
                
                try {
                    if(expediente.buscarExpedienteMedicoPorCedula(paciente.getCedula())!=null){
                        try {
                            numExpediente=String.valueOf(expediente.buscarExpedienteMedicoPorCedula(paciente.getCedula()).getNumExpediente());
                            jTxtNoExpediente.setText(numExpediente);
                        } catch (ParseException ex) {
                            Logger.getLogger(jDlgHistorialMedico.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        if(paciente.getGenero()=='F'){
                            jRdoBotFemenino.setSelected(true);
                        }else{
                            jRdoBotMasculino.setSelected(true);
                        }
                        try {
                            cargarJTable();
                        } catch (ParseException ex) {
                            Logger.getLogger(jDlgHistorialMedico.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Genere un expediente");
                    }   } catch (ParseException ex) {
                    Logger.getLogger(jDlgHistorialMedico.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Persona no existe");
            }

        }
    }//GEN-LAST:event_jTxtCedulaActionPerformed

